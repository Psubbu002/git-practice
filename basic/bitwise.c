#include<stdio.h>
int main()
{
    int a=10,b=20;
    printf("Bitwise AND output: %d\n",a&b); 
    printf("Bitwise OR output:%d\n",a|b);
    printf("Bitwise XOR output:%d\n",a^b);
    printf("Compliment output:%d\n",~10);
    printf("Right shift : %d\n",a>>2);
    printf("Left shift : %d\n",a<<2);
    return 0;
}
